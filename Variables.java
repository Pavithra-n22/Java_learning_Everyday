package org.example;

public class Variables {
    public static void main(String args[])
    {
        int var1;
        int var2;
        var1=1000;
        var2=2000;
        System.out.println(var1 +","+ var2);
        System.out.println(var1+var2);
        System.out.println(var2/var1);
        System.out.println(var2%var1);
        System.out.println(var2-var1);
    }
}
